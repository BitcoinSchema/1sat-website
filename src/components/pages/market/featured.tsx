export enum KnownSubType {
  Collection = "collection",
}

export type Collection = {
  txid: string;
  vout: number;
  outpoint: string;
  origin: string;
  height: number;
  idx: number;
  lock: string;
  SIGMA: any[];
  listing: boolean;
  bsv20: boolean;
  id: number;
  num: number;
  items?: string[];
  file: {
    hash: string;
    size: number;
    type: string;
  };
  MAP: {
    app: string;
    name: string;
    type: string;
    subType?: KnownSubType;
    royalties?: string;
    previewUrl?: string;
    subTypeData?: CollectionItem;
    [key: string]: any;
  };
};

interface GroupedCollection {
  name: string;
  collections: Collection[];
}

const FeaturedCollections: React.FC = () => {
  const [fetchFeaturedStatus, setFetchFeaturedStatus] = useState<FetchStatus>(
    FetchStatus.Idle
  );

  const [featuredCollections, setFeaturedCollections] = useState<Collection[]>(
    []
  );

  const groupedCollections: GroupedCollection[] = useMemo(() => {
    const grouped = groupBy(
      featuredCollections
        .filter((f) => {
          return (
            f.MAP.subType === KnownSubType.Collection &&
            featured.some((feature) => feature.origin === f.origin)
          );
        })
        .sort((a, b) => (a.height < b.height ? -1 : 1)),
      "MAP.name"
    ) as Record<string, Collection[]>;
    return map(grouped, (value, key) => ({ name: key, collections: value }));
  }, [featuredCollections]);

  useEffect(() => {
    const fire = async () => {
      try {
        setFetchFeaturedStatus(FetchStatus.Loading);
        const { promise } = customFetch<Collection[]>(
          `${API_HOST}/api/collections/recent`
        );

        const featuredCollections = await promise;
        setFeaturedCollections(featuredCollections.concat(ancientCollections));
        setFetchFeaturedStatus(FetchStatus.Success);
      } catch (error) {
        console.log(error);
        setFetchFeaturedStatus(FetchStatus.Error);
      }
    };
    if (fetchFeaturedStatus === FetchStatus.Idle) {
      fire();
    }
  }, [ancientCollections, fetchFeaturedStatus]);

  return (
    <div className="p-4 grid w-full mx-auto justify-center sm:grid-cols-2 lg:grid-cols-3 xl:grid-cols-4 gap-4">
      {groupedCollections.map((group) => {
        return (
          <div key={group.name}>
            <h2 className="my-4 text-lg font-semibold">{group.name}</h2>
            <div className="grid grid-cols-2 md:grid-cols-4 gap-4">
              {group.collections.map((item) => (
                <Artifact
                  key={item.origin}
                  origin={item.origin}
                  outPoint={item.outpoint}
                  contentType={item.file.type}
                  num={item.num}
                  to={`/collection/${item.origin}`}
                  src={item.MAP.previewUrl}
                  onClick={() => {}}
                  txid={item.txid}
                  price={0}
                  height={item.height}
                />
              ))}
            </div>
          </div>
        );
      })}
    </div>
  );
};

